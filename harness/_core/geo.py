# Import-time bounds check — catches coordinate typos immediately
assert all(
    33.0 <= lat <= 39.0 and 124.0 <= lng <= 130.0
    for lat, lng in KR_COORDS.values()
), "KR_COORDS contains coordinate(s) outside Korean bounding box [33-39N, 124-130E]"


def haversine_km(lat1: float, lng1: float, lat2: float, lng2: float) -> float:
    R = 6371
    p1, p2 = math.radians(lat1), math.radians(lat2)
    dp = math.radians(lat2 - lat1)
    dl = math.radians(lng2 - lng1)
    a = math.sin(dp / 2) ** 2 + math.cos(p1) * math.cos(p2) * math.sin(dl / 2) ** 2
    return 2 * R * math.asin(math.sqrt(a))


def estimate_dest_coord(addr: str) -> tuple[float, float] | None:
    if not addr:
        return None
    # Long patterns first — resolves ambiguities like "경기도 광주" vs "광주광역시"
    for name in sorted(KR_COORDS, key=len, reverse=True):
        if name in addr:
            return KR_COORDS[name]
    # Administrative unit regex extraction
    m = re.search(r"([가-힣]+(?:구|시|군))", addr)
    if m and m.group(1) in KR_COORDS:
        return KR_COORDS[m.group(1)]
    # Metropolitan city keyword fallback
    for city in ["인천", "대전", "대구", "부산", "울산", "세종"]:
        if city in addr:
            return KR_COORDS.get(city)
    if "광주" in addr:
        # 경기도 광주 already handled above; reaching here means 전남 광주
        return KR_COORDS["광주"]
    return None
